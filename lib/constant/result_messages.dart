  /// 指定言語・用途のメッセージ群から [seed] に基づいて1つ選ぶ。
  ///
  /// [seed] は結果画面ごとに一度だけ決め、再描画では変えないことで
  /// 同じ画面の中でメッセージがちらつかないようにする。
  static String pick({
    required ResultMessageKind kind,
    required String languageCode,
    required int seed,
  }) {
    final byKind = _messages[languageCode] ?? _messages['en']!;
    final list = byKind[kind] ?? _messages['en']![kind]!;
    return list[seed.abs() % list.length];
  }

