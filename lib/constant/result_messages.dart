//
// 用途（ほめ言葉・新記録・失敗の励まし）ごとに複数のバリエーションを持ち、
// 表示のたびにランダムで1つ選ぶことで、毎回同じ言葉にならないようにする。
// アプリが対応する9言語ぶんを用意し、未対応の言語は英語にフォールバックする。

/// 結果メッセージの用途。
enum ResultMessageKind {
  /// 全問正解（新記録ではない）時のほめ言葉。
  praise,

  /// タイムが自己ベストを更新した時の一言。
  newRecord,

  /// 失敗（不正解あり・あきらめ）時の励ましの一言。
  failure,
}

